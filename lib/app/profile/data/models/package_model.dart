import '../../domain/entities/package_entity.dart';

class PackageModel extends PackageEntity {
  PackageModel({
    required String id,
    required String packageNameEn,
    required String packageNameAr,
    required String packagePrice,
    required String serviceRequestCount,
    required String quotationCount,
    required String validityDays,
    required String isFreePackage,
    required String createdAt,
    required String updatedAt,
    required String active,
    required String isDeleted,
    required String sortOrderId,
    required String commission,
    required String stripePriceId,
    required String stripeProductId,
    required String noOfSkill,
  }) : super(
            id: id,
            packageNameEn: packageNameEn,
            packageNameAr: packageNameAr,
            packagePrice: packagePrice,
            serviceRequestCount: serviceRequestCount,
            quotationCount: quotationCount,
            validityDays: validityDays,
            isFreePackage: isFreePackage,
            createdAt: createdAt,
            updatedAt: updatedAt,
            active: active,
            isDeleted: isDeleted,
            sortOrderId: sortOrderId,
            commission: commission,
            stripePriceId: stripePriceId,
            stripeProductId: stripeProductId,
            noOfSkill: noOfSkill);

  factory PackageModel.fromJson(Map<String, dynamic> json) => PackageModel(
        id: json["id"],
        packageNameEn: json["package_name_en"],
        packageNameAr: json["package_name_ar"],
        packagePrice: json["package_price"],
        serviceRequestCount: json["service_request_count"],
        quotationCount: json["quotation_count"],
        validityDays: json["validity_days"],
        isFreePackage: json["is_free_package"],
        createdAt: DateTime.parse(json["created_at"]).toIso8601String(),
        updatedAt: DateTime.parse(json["updated_at"]).toIso8601String(),
        active: json["active"],
        isDeleted: json["is_deleted"],
        sortOrderId: json["sort_order_id"],
        commission: json["commission"],
        stripePriceId: json["stripe_price_id"],
        stripeProductId: json["stripe_product_id"],
        noOfSkill: json["no_of_skill"],
      );

  Map<String, dynamic> toJson() => {
        "id": id,
        "package_name_en": packageNameEn,
        "package_name_ar": packageNameAr,
        "package_price": packagePrice,
        "service_request_count": serviceRequestCount,
        "quotation_count": quotationCount,
        "validity_days": validityDays,
        "is_free_package": isFreePackage,
        "created_at": createdAt,
        "updated_at": updatedAt,
        "active": active,
        "is_deleted": isDeleted,
        "sort_order_id": sortOrderId,
        "commission": commission,
        "stripe_price_id": stripePriceId,
        "stripe_product_id": stripeProductId,
        "no_of_skill": noOfSkill,
      };
}
